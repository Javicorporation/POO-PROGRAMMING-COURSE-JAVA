package refactor;

public class ConstructorLargo {
    String nombre;
    String marca;
    String modelo;
    String motor;
    int puertas;


    public ConstructorLargo(String nombre, String marca, String modelo, String motor, int puertas) {
        this.nombre = nombre;
        this.marca = marca;
        this.modelo = modelo;
        this.motor = motor;
        this.puertas = puertas;
    }
}
